let currentLang = DEFAULT_LANG;

function getNested(obj, path) {
  return path.split(".").reduce((acc, key) => acc?.[key], obj);
}

export function getLang() {
  return currentLang;
}

export function t(key, lang = currentLang) {
  return getNested(translations[lang], key) ?? getNested(translations.en, key) ?? key;
}

export function categoryLabel(category, lang = currentLang) {
  return t(`categories.${category}`, lang) || category;
}

export function getHeroSlides(lang = currentLang) {
  return t("hero.slides", lang);
}

export function setLang(lang) {
  if (!translations[lang]) return;
  currentLang = lang;
  localStorage.setItem(STORAGE_KEY, lang);
  applyLanguage();
}

export function initLang() {
  const saved = localStorage.getItem(STORAGE_KEY);
  const normalized = saved === "sq" ? "al" : saved;
  currentLang = translations[normalized] ? normalized : DEFAULT_LANG;
  applyLanguage();
}

export function applyLanguage() {
  const lang = currentLang;
  document.documentElement.lang = lang === "al" ? "sq" : lang;

  document.title = t("meta.title", lang);
  const metaDesc = document.querySelector('meta[name="description"]');
  if (metaDesc) metaDesc.content = t("meta.description", lang);

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    el.textContent = t(el.dataset.i18n, lang);
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    el.placeholder = t(el.dataset.i18nPlaceholder, lang);
  });

  document.querySelectorAll("[data-i18n-aria]").forEach((el) => {
    el.setAttribute("aria-label", t(el.dataset.i18nAria, lang));
  });

  document.querySelectorAll(".lang-switch__btn").forEach((btn) => {
    btn.classList.toggle("is-active", btn.dataset.lang === lang);
    btn.setAttribute("aria-pressed", btn.dataset.lang === lang ? "true" : "false");
  });

  const marqueeTrack = document.querySelector(".marquee__track");
  if (marqueeTrack) {
    const items = t("marquee", lang);
    const star = '<span class="marquee__star">✦</span>';
    const doubled = [...items, ...items];
    marqueeTrack.innerHTML = doubled.map((text) => `<span>${text}</span>${star}`).join("");
  }

  window.dispatchEvent(new CustomEvent("languagechange", { detail: { lang } }));
}
